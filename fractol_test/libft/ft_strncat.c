#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	g;
	char	*s;

	s = s1;
	g = 0;
	i = 0;
	while (s[i])
		i++;
	while (n-- > 0 && s2[g] != '\0')
		s[i++] = s2[g++];
	s[i] = '\0';
	return (s);
}

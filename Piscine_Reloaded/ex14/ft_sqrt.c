int		ft_sqrt(int nb)
{
	int	sq;

	sq = 1;
	if (nb <= 0)
		return (0);
	while (sq < 46342)
	{
		if ((sq * sq) == nb)
			return (sq);
		sq++;
	}
	return (0);
}

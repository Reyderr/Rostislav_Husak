#include "libft.h"

void	ft_putnbr(int n)
{
	long	int i;
	long	int z;
	int			r;

	r = 1;
	z = n;
	if (z < 0)
	{
		ft_putchar('-');
		z = -z;
	}
	i = z;
	while ((i /= 10) > 0)
		r *= 10;
	i = z;
	while (r)
	{
		ft_putchar((i / r) + 48);
		i %= r;
		r /= 10;
	}
}
